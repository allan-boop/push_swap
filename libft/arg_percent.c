#include "libft.h"

t_flags	arg_percent(t_flags *ret)
{
	ret->charac++;
	if (write(1, "%", 1) == -1)
	{
		ret->len = -1;
		return (*ret);
	}
	ret->len++;
	return (*ret);
}
